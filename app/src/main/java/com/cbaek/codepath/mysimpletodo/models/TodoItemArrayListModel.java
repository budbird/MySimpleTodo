package com.cbaek.codepath.mysimpletodo.models;


public class TodoItemArrayListModel {
    public String itemName;
    public String priority;
    public int year;
    public int month;
    public int day;


    public TodoItemArrayListModel(String itemName, String priority, int year, int month, int day) {
        this.itemName = itemName;
        this.priority = priority;
        this.year = year;
        this.month = month;
        this.day = day;
    }

}
